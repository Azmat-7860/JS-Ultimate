function forLoop() {

    let cars = ["Bimmer", "Audi", "Toyota", "Pagani"];

    // for loop :: is used when you know the number of iteration
    // It consists of three parts: initialization, condition, and increment (or decrement). 
    for (let i = 0; i < cars.length; i++)
        console.log(cars[i]);

}
forLoop();